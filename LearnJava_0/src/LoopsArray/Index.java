package LoopsArray;

public class Index {
    public static void main(String[] args) {
        /**
         * float[] vals = new float[3]; vals[0] = 10; vals[1] = 20; vals[2] = 30;
         */

        float[] vals = { 10, 20, 30 };

        for (int i = 0; i < vals.length; i++) {
            System.out.println(vals[i]);
        }
    }
}
